Question :7 Create a daily cumulative sales report for 2019. Each day should have previous days sale amount added.

Answer:
Select transfer_date, price, sum(price) over(order by transfer_date) as runningPrice from land_registry_price_paid_uk where extract(year from transfer_date)='2019' ;


